export function AdminSidebar({ mobileOpen = false, onClose }: AdminSidebarProps) {
  const pathname = usePathname();

  // Swipe-to-close tracking
  const touchStartX = useRef<number>(0);
  const touchCurrentX = useRef<number>(0);
  const drawerRef = useRef<HTMLDivElement>(null);

  const handleTouchStart = useCallback((e: React.TouchEvent) => {
    touchStartX.current = e.touches[0].clientX;
    touchCurrentX.current = e.touches[0].clientX;
  }, []);

  const handleTouchMove = useCallback((e: React.TouchEvent) => {
    touchCurrentX.current = e.touches[0].clientX;
    const deltaX = touchCurrentX.current - touchStartX.current;
    if (deltaX < 0 && drawerRef.current) {
      drawerRef.current.style.transform = `translateX(${deltaX}px)`;
    }
  }, []);

  const handleTouchEnd = useCallback(() => {
    const deltaX = touchCurrentX.current - touchStartX.current;
    if (drawerRef.current) {
      drawerRef.current.style.transform = '';
    }
    if (deltaX < -80 && onClose) {
      onClose();
    }
  }, [onClose]);

  const SidebarContent = () => (
    <>
      {/* Header */}
      <Link href="/admin" className="p-4 lg:p-6 border-b border-[var(--color-bg)]/10 hover:opacity-80 transition-opacity">
        <div className="flex items-center gap-3">
          <Image src="/logo.webp" alt="ZKIRA Pay" width={101} height={40} className="h-[28px] w-auto brightness-0 invert" priority />
          <span className="px-2 py-1 bg-[var(--color-surface)]/10 text-[var(--color-bg)] text-xs rounded font-medium">
            Admin
          </span>
        </div>
      </Link>

      {/* Navigation */}
      <nav className="flex-1 px-3 lg:px-4 py-4 lg:py-6 space-y-1 overflow-y-auto">
        {navigation.map((item) => {
          const isActive = pathname === item.href || (item.href !== '/admin' && pathname?.startsWith(item.href));
          return (
            <Link
              key={item.name}
              href={item.href}
              onClick={onClose}
              className={`flex items-center gap-3 px-3 py-3 text-sm font-medium transition-colors ${
                isActive
                  ? 'bg-[var(--color-surface)]/10 text-[var(--color-bg)]'
                  : 'text-[var(--color-bg)]/70 hover:bg-[var(--color-surface)]/5 hover:text-[var(--color-bg)]'
              }`}
            >
              {item.icon}
              {item.name}
            </Link>
          );
        })}

        {/* Website Section Divider */}
        <div className="pt-4 pb-2">
          <span className="px-3 text-xs font-semibold text-[var(--color-bg)]/40 uppercase tracking-wider">Website</span>
        </div>

        {/* Website Navigation */}
        {websiteNavigation.map((item) => {
          const isActive = pathname === item.href || (item.href !== '/admin' && pathname?.startsWith(item.href));
          return (
            <Link
              key={item.name}
              href={item.href}
              onClick={onClose}
              className={`flex items-center gap-3 px-3 py-3 text-sm font-medium transition-colors ${
                isActive
                  ? 'bg-[var(--color-surface)]/10 text-[var(--color-bg)]'
                  : 'text-[var(--color-bg)]/70 hover:bg-[var(--color-surface)]/5 hover:text-[var(--color-bg)]'
              }`}
            >
              {item.icon}
              {item.name}
            </Link>
          );
        })}

        {/* Operations Section Divider */}
        <div className="pt-4 pb-2">
          <span className="px-3 text-xs font-semibold text-[var(--color-bg)]/40 uppercase tracking-wider">Operations</span>
        </div>

        {/* Operations Navigation */}
        {opsNavigation.map((item) => {
          const isActive = pathname === item.href || (item.href !== '/admin' && pathname?.startsWith(item.href));
          return (
            <Link
              key={item.name}
              href={item.href}
              onClick={onClose}
              className={`flex items-center gap-3 px-3 py-3 text-sm font-medium transition-colors ${
                isActive
                  ? 'bg-[var(--color-surface)]/10 text-[var(--color-bg)]'
                  : 'text-[var(--color-bg)]/70 hover:bg-[var(--color-surface)]/5 hover:text-[var(--color-bg)]'
              }`}
            >
              {item.icon}
              {item.name}
            </Link>
          );
        })}

        {/* Rewards Section Divider */}
        <div className="pt-4 pb-2">
          <span className="px-3 text-xs font-semibold text-[var(--color-bg)]/40 uppercase tracking-wider">Rewards</span>
        </div>

        {/* Rewards Navigation */}
        {rewardsNavigation.map((item) => {
          const isActive = pathname === item.href || (item.href !== '/admin' && pathname?.startsWith(item.href));
          return (
            <Link
              key={item.name}
              href={item.href}
              onClick={onClose}
              className={`flex items-center gap-3 px-3 py-3 text-sm font-medium transition-colors ${
                isActive
                  ? 'bg-[var(--color-surface)]/10 text-[var(--color-bg)]'
                  : 'text-[var(--color-bg)]/70 hover:bg-[var(--color-surface)]/5 hover:text-[var(--color-bg)]'
              }`}
            >
              {item.icon}
              {item.name}
            </Link>
          );
        })}
      </nav>

      {/* Footer */}
      <div className="p-3 lg:p-4 border-t border-[var(--color-bg)]/10">
        <Link
          href="/"
          onClick={onClose}
          className="flex items-center gap-3 px-3 py-3 text-sm font-medium text-[var(--color-bg)]/70 hover:bg-[var(--color-surface)]/5 hover:text-[var(--color-bg)] transition-colors"
        >
          <svg className="w-[18px] h-[18px]" fill="none" stroke="currentColor" strokeWidth="1.5" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" d="M10.5 19.5L3 12m0 0l7.5-7.5M3 12h18" />
          </svg>
          Back to App
        </Link>
      </div>
    </>
  );

  return (
    <>
      {/* Desktop Sidebar */}
      <div className="hidden md:flex md:flex-col md:w-48 lg:w-60 bg-[var(--color-button)] h-full shrink-0">
        <SidebarContent />
      </div>

      {/* Mobile Backdrop */}
      {mobileOpen && (
        <div
          className="fixed inset-0 z-40 bg-black/30 backdrop-blur-sm md:hidden"
          onClick={onClose}
        />
      )}

      {/* Mobile Drawer */}
      <div
        ref={drawerRef}
        onTouchStart={handleTouchStart}
        onTouchMove={handleTouchMove}
        onTouchEnd={handleTouchEnd}
        className={`fixed inset-y-0 left-0 z-50 w-64 bg-[var(--color-button)] flex flex-col transform transition-transform duration-300 ease-out md:hidden touch-pan-y ${
          mobileOpen ? 'translate-x-0' : '-translate-x-full'
        }`}
      >
        {/* Close button */}
        <button
          onClick={onClose}
          className="absolute top-4 right-4 p-2 text-[var(--color-bg)]/60 hover:text-[var(--color-bg)] z-10"
        >
          <svg className="w-5 h-5" fill="none" stroke="currentColor" strokeWidth="1.5" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" d="M6 18L18 6M6 6l12 12" />
          </svg>
        </button>
        <SidebarContent />
      </div>
    </>
  );
}